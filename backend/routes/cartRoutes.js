// @route POST/api/cart/merge
// @desc Merge guest cart into the user on login
// @access Private

router.post("/merge", protect, async (req, res) => {
  const { guestId } = req.body;

  try {
    const guestCart = await Cart.findOne({ guestId });
    const userCart = await Cart.findOne({ user: req.user._id }); // âœ… Corrected

    if (guestCart) {
      if (guestCart.products.length === 0) {
        return res.status(400).json({ message: "Guest cart is empty" });
      }

      if (userCart) {
        // Merge guest cart into user cart
        guestCart.products.forEach((guestItem) => {
          const productIndex = userCart.products.findIndex(
            (item) =>
              item.productId.toString() === guestItem.productId.toString() &&
              item.size === guestItem.size &&
              item.color === guestItem.color
          );

          if (productIndex > -1) {
            userCart.products[productIndex].quantity += guestItem.quantity;
          } else {
            userCart.products.push(guestItem);
          }
        });

        userCart.totalPrice = userCart.products.reduce(
          (acc, item) => acc + item.price * item.quantity,
          0
        );

        await userCart.save();

        try {
          await Cart.findOneAndDelete({ guestId }); // delete guest cart
        } catch (err) {
          console.error("Error deleting guest cart:", err);
        }

        return res.status(200).json(userCart); // âœ… Return the updated user cart
      } else {
        // No existing user cart â€” convert guest cart to user cart
        guestCart.user = req.user._id;
        guestCart.guestId = undefined;

        await guestCart.save();
        return res.status(200).json(guestCart);
      }
    } else {
      // If no guest cart
      const fallbackCart = await Cart.findOne({ user: req.user._id });
      if (fallbackCart) {
        return res.status(200).json(fallbackCart);
      }
      res.status(404).json({ message: "Guest cart not found" });
    }
  } catch (error) {
    console.error("MERGE CART ERROR:", error);
    res.status(500).json({ message: "Server ERROR", error: error.message });
  }
});

module.exports = router;
